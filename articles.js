function openArticleInPopup(link) {
  const popup = document.getElementById('articlePopup'); // Get the popup div
  const articleIframe = document.getElementById('articleIframe'); // Get the iframe inside the popup

  // Set the iframe source to the article link
  articleIframe.src = link;

  // Display the popup
  popup.style.display = 'block';
}

function closePopup() {
  const popup = document.getElementById('articlePopup'); // Get the popup div

  // Hide the popup
  popup.style.display = 'none';
}

function generateArticleCards() {
  const articles = getTourCards();
  const articleContainer = document.getElementById('articleRow');

  articles.forEach((article, index) => {
    const card = document.createElement('div');
    card.className = 'card';

    const image = document.createElement('img');
    image.src = article.image;
    image.className = 'card-img-top';

    const cardBody = document.createElement('div');
    cardBody.className = 'card-body';

    const title = document.createElement('h5');
    title.className = 'card-title recent-article-title';
    title.textContent = article.title;

    const summary = document.createElement('p');
    summary.className = 'card-text recent-article-summary';
    summary.textContent = article.summary;


    const creator = document.createElement('p');
    creator.className = 'card-text';
    creator.textContent = 'By ' + article.creator;

    const link = document.createElement('a');
    link.href = 'javascript:void(0)'; // This prevents the default link behavior
    link.className = 'btn btn-primary';
    link.textContent = 'Read More';
    link.addEventListener('click', () => openArticleInPopup(article.link)); // Open in popup

    cardBody.appendChild(title);
    cardBody.appendChild(summary);
    cardBody.appendChild(creator);
    cardBody.appendChild(link);

    card.appendChild(image);
    card.appendChild(cardBody);

    articleContainer.appendChild(card);
  });
}

// Call the generateArticleCards function when the page loads
generateArticleCards();

// Export the function to make it accessible from other scripts
if (typeof module !== 'undefined' && typeof module.exports !== 'undefined') {
  module.exports = { getTourCards };
}
